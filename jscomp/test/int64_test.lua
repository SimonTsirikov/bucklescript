function fib(_n, _a, _b) do
  while(true) do
    b = _b;
    a = _a;
    n = _n;
    if (n == 0) then do
      return a;
    end else do
      _b = Caml_int64.add(a, b);
      _a = b;
      _n = n - 1 | 0;
      ::continue:: ;
    end end 
  end;
end end

function fac(_n, _acc) do
  while(true) do
    acc = _acc;
    n = _n;
    if (n == 0) then do
      return acc;
    end else do
      _acc = Caml_int64.mul(acc, Caml_int64.of_int32(n));
      _n = n - 1 | 0;
      ::continue:: ;
    end end 
  end;
end end

